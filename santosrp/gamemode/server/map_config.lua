local DeletePropModels = {
	"models/props_equipment/gas_pump.mdl",
}

hook.Add("InitPostEntity","SpawnMapEnts",function()
	local Map = game.GetMap()
	--if (game.GetMap():lower() != "rp_rockford_v1b") then return end
	
	for k,v in pairs(DeletePropModels) do
		for a,ent in pairs(ents.FindByModel(v)) do
			ent:Remove()
		end
	end
	
	for k,v in pairs(List) do
		local e = ents.Create(v.Class)
		
		if (IsValid(e)) then
			e:SetPos(v.Pos)
			e:SetAngles(v.Ang)
			e:Spawn()
			e:Activate()
			
			local Phys = e:GetPhysicsObject()
			
			if (IsValid(Phys)) then
				Phys:EnableMotion(false)
				Phys:Sleep()
			end
			
			e:SetMoveType(MOVETYPE_NONE)
			
			e.CreatedByMap = function() return true end
		else
			MsgN("Entity "..v.Class.." could not be created!")
		end
	end
end)
	